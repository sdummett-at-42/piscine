void	ft_sort_int_tab(int *tab, int size)
{
	int temp;
	int i;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] <= tab[i + 1])
		{
			i++;
		}
		else
		{
			temp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = temp;
			i = 0;
		}
	}
}
