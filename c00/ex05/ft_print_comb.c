#include <unistd.h>

void ft_print_comb(void) 
{
	int d = 0;
	int e = 0;
	int f = 0;
	char c;

	while (d < 10) {
		e = d + 1;
		while (e < 10) {
			f = e + 1;
			while (f < 10) {
				if (f != 9 || e != 8 || d != 7){
					c = d + 48;
					write(1, &c, 1);
					c = e + 48;
					write(1, &c, 1);
					c = f + 48;
					write(1, &c, 1);
					write(1, ", ", 2);	
				}
				else {
					c = d + 48;
					write(1, &c, 1);
					c = e + 48;
					write(1, &c, 1);
					c = f + 48;
					write(1, &c, 1);
				}
				f++;
			}
			e++;
		}
		d++;
	}
}
